/*
  Write a program that takes as input two numbers and print the sum.

  Output:
  Insert the first number: 1
  Insert the second number: 2
  Sum: 3
*/

#include <iostream>
using namespace std;
 int main(){
  int x1, x2;
  cout << "Insert the first number: ";
  cin >> x1;
  cout << endl;
  cout << "Insert the second number: ";
  cin >> x2;
  cout << endl;
  cout << "Sum: " << x1+x2 << endl;
 }